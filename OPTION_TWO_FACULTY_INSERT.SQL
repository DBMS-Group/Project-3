CREATE OR REPLACE PROCEDURE OPTION_TWO_FACULTY_INSERT 
(
  FID IN NUMBER 
, FNAME IN VARCHAR2
, DEPT_ID IN NUMBER
, EXCLUDING_DEPT_ID IN NUMBER
)
AS
  average number(20);
BEGIN
  SELECT AVG(FACULTY.SALARY) INTO average FROM FACULTY WHERE FACULTY.DEPTID != EXCLUDING_DEPT_ID;
  INSERT INTO FACULTY VALUES(FID, FNAME, DEPT_ID, average);
END OPTION_TWO_FACULTY_INSERT;